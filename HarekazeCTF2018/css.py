chars.append("_")

final = []
for co in couple:
    if co[0] == co[1]:
        continue
    match = [0]*len(chars)
    cotext = co[0]+"_"+co[1]
    
    for c in "arekaze":
        try:
            ind = chars.index(c)
            match[ind] = 1
        except:
            pass
    
